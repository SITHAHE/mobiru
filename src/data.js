// Округление до «красивой» цены, оканчивающейся на 90
function nice(x) {
  const r = Math.round(x / 100) * 100
  return r <= 990 ? 890 : r - 10
}

// Стоимость конкретной работы для модели (анкер d)
export function priceFor(model, repairId) {
  const rep = REPAIRS.find((r) => r.id === repairId)
  if (!rep) return null
  if (rep.flat) return rep.flat
  const d = model.d
  switch (repairId) {
    case 'display': return d
    case 'glass': return nice(d * 0.62)
    case 'battery': return nice(Math.max(d * 0.42, 890))
    case 'port': return nice(Math.max(d * 0.38, 890))
    case 'camera': return nice(Math.max(d * 0.5, 1190))
    case 'water': return nice(Math.max(d * 0.55, 1490))
    case 'back': return nice(Math.max(d * 0.7, 1290))
    default: return d
  }
}

export function fmt(n) {
  return new Intl.NumberFormat('ru-RU').format(n) + ' ₽'
}

// Минимальная цена «от» по всем моделям для конкретной работы —
// честный нижний порог для карточек «популярные цены».
export function minPriceFor(repairId) {
  let min = Infinity
  for (const b of BRANDS) for (const m of b.models) {
    const p = priceFor(m, repairId)
    if (p != null && p < min) min = p
  }
  return min
}

